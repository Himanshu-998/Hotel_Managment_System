    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        boolean fl = validateID(id);
        if(fl==false)
        {
            new ErrorDialog("Wrong " + jComboBox1.getSelectedItem() + " entered!");
        }
        else
        {
             /* means entered ID is valid,now enter details in booking database, update roomcount in hotellist
            and generate a booking reference number for the user!*/
            /*We're not allowing one user to have multiple bookings in his name!*/
            // First update roomcount in hotellist portal
            try
            {
            RS3 = ob2.rs;
            while(RS3.next())
            {
                if(RS3.getString("name").equals(hotelname)&&RS3.getString("city").equals(location))
                {
                    int presentroomcount = RS3.getInt("roomcount");
                    int newroomcount = presentroomcount-rooms;
                    RS3.updateInt("roomcount",newroomcount);
                    RS3.updateRow();
                    break;
                }
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
            ResultSet RS4 = ob.rs;
            date = new Date();// holds current date i.e date of booking
            int c = 0;
            DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");  
            try
            {
                RS4.beforeFirst();
                RS4.moveToInsertRow();
                RS4.updateString("username",username);
                RS4.updateString("location",location);
                RS4.updateString("hotelname",hotelname);
                RS4.updateString("checkin",dateFormat.format(Checkin));
                RS4.updateString("checkout",dateFormat.format(Checkout));
                RS4.updateInt("rooms",rooms);
                RS4.updateString("roomtype",roomtype);
                RS4.updateString("bookingdate",dateFormat.format(date));
                RS4.updateString("idtype",jComboBox1.getSelectedItem().toString());
                RS4.updateString("id",id);
                RS4.insertRow();
                
                // Check for booking reference number must be shown!!
                ob = new JDBCConnections("SELECT * FROM bookinglist where username = '"+username+"'","hoteldetails");
                ResultSet rs = ob.rs;
                rs.afterLast();
                rs.previous();
                new MessageDialog(jButton2,"BookHotel","SUCCESS!","Booking successfully completed!Your booking reference number is: "+ rs.getInt("bookingrefnumber"));
              
                RS3.close();
                RS4.close();
            }
            catch(Exception e)
            {
                System.out.println(e);
            }
    }//GEN-LAST:event_jButton2ActionPerformed
    }
