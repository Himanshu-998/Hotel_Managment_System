    boolean validateID(String id)
    {
        if(jComboBox1.getSelectedItem().equals("Adhaar UID"))
        {
            // Introduce the check that it is a 12 digit number only!
            Pattern pattern = Pattern.compile("\\d{12}");
            if(pattern.matcher(id).matches())
            {
                return true;
            }
            else
            {
                return false;
            }
        }
        else
        {
            Pattern pattern = Pattern.compile("([A-Z]{5})(\\d{4})([A-Z])");
            if(pattern.matcher(id).matches())
                return true;
            else 
                return false;
        }
    }
    
